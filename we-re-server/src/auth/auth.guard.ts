import {
  CanActivate,
  ExecutionContext,
  Injectable,
  Logger,
  UnauthorizedException,
} from '@nestjs/common';
import { ConfigService } from '@nestjs/config';
import { JwtService } from '@nestjs/jwt';
import { Request } from 'express';
import { CustomUnauthorziedException } from 'src/utils/custom_exceptions';

const SECRET_KEY_TYPE = {
  REFRESH: 'REFRESH_SECRET_KEY',
  ACCESS: 'ACCESS_SECRET_KEY',
};

@Injectable()
export class AccessTokenGuard implements CanActivate {
  constructor(
    private readonly jwtService: JwtService,
    private readonly configService: ConfigService,
  ) {}

  async canActivate(context: ExecutionContext): Promise<boolean> {
    const request = context.switchToHttp().getRequest();
    const token = this.extractTokenFromHeader(request);
    if (!token) {
      throw new UnauthorizedException();
    }
    try {
      const payload = await this.jwtService.verifyAsync(token, {
        secret: this.configService.get<string>(SECRET_KEY_TYPE.ACCESS),
      });
      Logger.log(payload);
      request['userId'] = payload.userId;
    } catch {
      throw new CustomUnauthorziedException('Access Token is unvalid.');
    }
    return true;
  }

  public extractTokenFromHeader(request: Request): string | undefined {
    const [type, token] = request.headers.authorization?.split(' ') ?? [];
    return type === 'Bearer' ? token : undefined;
  }
}

